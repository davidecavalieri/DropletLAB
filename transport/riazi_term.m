function [b, c, Pr] = riazi_term(Species,X,P)


% Critical pressures and acentric factors
Pc = cell2mat({Species.Pc});
omega = cell2mat({Species.omega});
%% PSEUDO-CRITICAL PROPERTIES KAY'S RULE

Pcm    = sum(X.*Pc);
omegam = sum(X.*omega);

b = -0.27 - 0.38* omegam;
c = -0.05 + 0.1* omegam;
Pr = P/Pcm;

end